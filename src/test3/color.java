package test3;

public class color {
	private int red;
	private int green;
	private int blue;
	public color()
	{
		red=0;
		green=0;
		blue=0;
	}
	public color(int r,int g,int b)
	{
		this.red=r;
		this.green=g;
		this.blue=b;
	}
	public int getRed() {
		return red;
	}
	public void setRed(int red) {
		this.red = red;
	}
	public int getGreen() {
		return green;
	}
	public void setGreen(int green) {
		this.green = green;
	}
	public int getBlue() {
		return blue;
	}
	public void setBlue(int blue) {
		this.blue = blue;
	}
	
	

}
